weather = "chilly"

case weather
when "sunny"
puts "It's nice and sunny out"
when "chilly"
puts "Grab your coat, it's a chilly one!"
when "rainy"
puts "You might want to bring your umbrella, stay dry!"
when "foggy"
puts "Take it slow, it's foggy"
when "frigid"
puts "You sure you want to go out there, it's freezing!"
end